//Creating an Array
var marks = [90, 68, 89, 99, 55, 77, 82];
console.log(marks);
console.log(marks[3]);

//You can also create an array, and then provide the elements
let fruits = [];
fruits[0] = "Apple";
fruits[1] = "Lemon";
fruits[2] = "Banana";
console.log(fruits);
console.log(fruits[1]);

//Using the JavaScript Keyword new
const cars = new Array("Tesla", "BMW", "Ferrari", "Ford", "Porsche");
console.log(cars);
console.log(cars[2]);
