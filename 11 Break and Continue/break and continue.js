// Break Statement
console.log(" - - - - - - - - - - Break Statement - - - - - - - - - - ");
for (let b = 0; b < 5; b++) {
    if (b == 3) { break; }
    console.log(b);
}

// Continue Statement
console.log(" - - - - - - - - - - Continue Statement - - - - - - - - - - ");
for (let c = 0; c < 5; c++) {
    if (c == 2) { continue; }
    console.log(c);
}